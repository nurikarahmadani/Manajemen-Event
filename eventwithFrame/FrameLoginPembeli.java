    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Pembeli obj = new Pembeli();
        obj.login(txtNama.getText(), txtPassword.getText());

        try {
            String sql = "select * from pembeli where nama='"+ txtNama.getText() +"'";
            java.sql.Connection conn = (Connection) event.config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while(res.next()){
                this.id = Integer.parseInt(res.getString("id"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        if (obj.status == 1) {
            FramePembeli frame = new FramePembeli(this.id);
            frame.setVisible(true);
            frame.setSize(750, 500);
            frame.setTitle("Program Manajemen Event");
            frame.setLocationRelativeTo(null);//menentukan letak frame ditengah saat dirun
            frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            frame.setResizable(true);

            this.dispose();
        } else if (obj.status == 0) {
            JOptionPane.showMessageDialog(null, "Username atau Password Salah");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

