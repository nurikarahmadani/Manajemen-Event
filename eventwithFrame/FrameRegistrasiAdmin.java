    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int id = Integer.parseInt(txtId.getText());
            Admin obj = new Admin();
            obj.registrasi(id, txtNama.getText(), txtPassword.getText());
            if (obj.berhasilRegis = true) {
                JOptionPane.showMessageDialog(null, "Berhasil Registrasi");
            } else if (obj.berhasilRegis = false) {
                JOptionPane.showMessageDialog(null, "Gagal Registrasi");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Gagal !!!\nTolong masukkan data angka pada Id dan Password !!!");

        }

    }//GEN-LAST:event_jButton1ActionPerformed

