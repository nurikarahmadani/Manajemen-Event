    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            int id = Integer.parseInt(txtId.getText());
            Pembeli obj = new Pembeli();
            Random saldoRandom = new Random();
            int saldoReg = 500000 + saldoRandom.nextInt(100000000);
            obj.registrasi(id, txtNama.getText(), txtPassword.getText(), saldoReg);
            if (obj.berhasilRegis = true) {
                JOptionPane.showMessageDialog(null, "Berhasil Registrasi");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal Registrasi");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Gagal !!!\nTolong masukkan data angka pada Id dan Password !!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

